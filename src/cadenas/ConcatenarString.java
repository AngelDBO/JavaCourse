package cadenas;

public class ConcatenarString {
    public static void main(String[] args) {
        String primerNombre = "Angel\t";
        String segundoNombre = "Daniel";
        System.out.println("primerNombre.concat(segundoNombre) = " + primerNombre.concat(segundoNombre));
        System.out.println("Nombre completo = " + primerNombre + segundoNombre);
    }
}
