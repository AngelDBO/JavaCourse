package variables.primitivos;

public class TipoDouble {
    public static void main(String[] args) {
        double numero = 1112.232D;
        System.out.println("EL numero double es: " + numero);
        System.out.println("EL double corresponde a: " + Double.BYTES);
        System.out.println("El double corresponde en bites a:" + Double.SIZE);
        System.out.println("EL valor double maximo es: " + Double.MAX_VALUE);
        System.out.println("EL valor double minimo es: " + Double.MIN_VALUE);
    }
}
